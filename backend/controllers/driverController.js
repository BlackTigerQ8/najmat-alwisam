// @desc    Update driver work
// @route   PATCH /api/drivers/:id/invoice
// @access  Private/Admin_and_Employee
const createDriverInvoice = async (req, res) => {
  try {
    const {
      driverId,
      hour,
      order,
      cash,
      additionalSalary,
      deductionReason,
      deductionAmount,
    } = req.body;
    const invoiceDate = new Date();
    const driver = await Driver.findById(driverId);

    if (!driver)
      return res.status(404).json({ message: "Driver does not exist" });

    const user = await User.findById(req.user.id);

    if (!user) return res.status(404).json({ message: "User does not exist" });

    const newInvoice = new DriverInvoice({
      driver: driverId,
      order,
      hour,
      cash,
      additionalSalary,
      deductionAmount,
      deductionReason,
      invoiceDate,
      user: req.user.id,
    });

    await newInvoice.save();

    return res.status(201).json({
      status: "Success",
      data: {
        driver: newInvoice,
      },
    });
  } catch (error) {
    res.status(500).json({
      status: "Error",
      message: error.message,
    });
  }
};

